		<h3 class="postbox-header">All About Page Templates</h3>
	<div class="inside">
	<p>Individual Pages can be set to use a specific custom <b>Page Template</b> that has been created within your Theme.  When you select this new Page Template it will then override the default <tt>page.php</tt> Page Template included with your Theme.</p>
	<p>WordPress can be configured to use <b>different Page Templates for different Pages.</b>  On the right of your page edit scrren is a drop-down labeled "Page Template."  From there you can select which Template will be used when displaying this particular Page. 
</p><p>Your theme includes a number of different page templates. The tabs above will have info on each of the different page templates included with your theme.</p>
	<h4>Default Theme Page Templates</h4>
	<p>The Default theme contains three Page Templates for your use:</p>
<p>	
<ol>
	<li><tt>page.php</tt> - Default Page Template: displays Page content</li>
	<li><tt>archives.php</tt> - ignores Page content and instead displays a list of Archives by Month and Archives by Subject (by Category)</li>
	<li><tt>links.php</tt> - ignores Page content and instead displays your links using <a title="" href="http://codex.wordpress.org/Template_Tags/wp_list_bookmarks">wp_list_bookmarks()</a></li>
</ol>
</p>	
	<h4>What Template is used to Display a Particular Page?</h4>
<p>WordPress will look for several template files in your active Theme. The first one it finds will be used to display any given Page. WordPress will look for files in the following order:
</p><p><ol>
	<li>The Page's selected "Page Template"</li>
	<li><tt>page.php</tt></li>
	<li><tt>index.php</tt></li>
</ol>
	 </p>
	<h4>Using a Page as the Front Page</h4>
	<p>A Page can easily be set to be your site's Front Page. Visit the Administration > Settings > Reading panel and under Front page displays, you can choose to set any (published) Page or Posts Page as the Front Page. The default setting shows your blog with the latest blog posts.
	</p>
	<p>See the <b>Magazine Templates</b> tab for instructions on using a page template as your home page </p>
