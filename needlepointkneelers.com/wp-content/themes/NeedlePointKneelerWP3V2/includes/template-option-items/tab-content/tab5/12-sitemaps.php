		<h3 class="postbox-header">Site Map Page Templates</h3>
	<div class="inside">
	
	<p>The Site Map page templates couldn't be simpler. Just create a new page, give it a name and choose one of the Site Map templates. </p>
	<p>No content is needed. The template creates the Site Map from list_categories, list_pages, get_archive. It also does a query of the latest 100 post plus adds some RSS links. All styled to the theme.</p>
	<p>Site Map templates 1 and 2 are the same with the exception of the style of the block titles. Try both and see which one you like the best. </p>
	<p>The third Site Map uses the <a title="Opens in new window" href="http://astuteo.com/slickmap/" target="_blank">SlickMap CSS</a> technique for making a visual Site Map. Give it a try. The colors used are the default for SlickMap.</p>
	
